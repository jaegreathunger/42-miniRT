#include "../includes/ft_printf.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *s)
{
	while (*s)
	{
		ft_putchar(*s);
		s++;
	}
}

void	ft_up_putstr(char *s)
{
	while (*s)
	{
		if ('a' <= *s && *s <= 'z')
			ft_putchar(*s - 32);
		else
			ft_putchar(*s); 
		s++;
	}
}

size_t	ft_strlen(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
